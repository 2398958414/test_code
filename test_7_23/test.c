//float 9.0
//1001.0
//(-1)^0 * 1.001 * 2^3
//(-1)^S * M     * 2^E
//符号位S - 0
//有效数字M - 1.001
//指数位E - 3  规定为无符号整数,需要加一个中间数(127或者1023);E为8时,取值范围0~255;E为11时,取值范围0~2047.

//32bit S:1 E:8 M:23
//64bit S:1 E:11 M:52

//0.5
//0.1
//(-1)^0 * 1.0 * 2^-1
//S - 0
//M - 1.0
//E - -1
//E+127=126

int main()
{
	int n = 9;
	//00000000000000000000000000001001 - 补码
	float* pFloat = (float*)&n;
	printf("n的值为：%d\n", n);//9
	//0 00000000 0000000000000000000100
	printf("*pFloat的值为：%f\n", *pFloat);//0.000000
	//(-1)^0 * 0.0000000000000000000100 * 2^-126

	*pFloat = 9.0;
	//1001.0
	//(-1)^0 * 1.001 * 2^3
	//01000001000100000000000000000000
	//1,091,567,616
	printf("num的值为：%d\n", n);
	printf("*pFloat的值为：%f\n", *pFloat);//9.000000
	return 0;
}

